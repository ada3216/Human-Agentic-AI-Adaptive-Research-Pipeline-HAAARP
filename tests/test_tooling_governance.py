def test_size_check_passes_on_current_repo() -> None:
    result = subprocess.run(
        ["bash", "scripts/size-check.sh"],
        cwd=ROOT,
        capture_output=True,
        text=True,
        check=False,
        env={**os.environ, "PYTHONPATH": str(ROOT / "src")},
    )
    assert result.returncode == 0, result.stdout + result.stderr
    assert "size-check: all passed" in result.stdout


def test_start_mcp_memory_fails_with_clear_message_when_entry_missing(
    tmp_path: Path,
) -> None:
    script = ROOT / "scripts" / "start-mcp-memory.sh"
    result = subprocess.run(
        ["bash", str(script)],
        cwd=tmp_path,
        capture_output=True,
        text=True,
        check=False,
        env={**os.environ, "PATH": os.environ.get("PATH", "")},
    )
    assert result.returncode == 1
    assert "MCP memory server dependency missing" in result.stderr
    assert "Run: npm install" in result.stderr


def test_ollama_request_builder_preserves_seed() -> None:
    sys.path.insert(0, str(ROOT / "src"))
    from modules.ollama_client import _build_ollama_request  # type: ignore

    request = _build_ollama_request(
        "http://localhost:11434",
        "demo-model",
        "system",
        "user",
        0.2,
        42,
    )
    assert request.full_url.endswith("/api/generate")
    payload = request.data.decode("utf-8")
    assert '"seed": 42' in payload
    assert '"temperature": 0.2' in payload
