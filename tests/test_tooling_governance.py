"""Tooling-governance canaries for lint, MCP, size checks, and workflow docs."""

from __future__ import annotations

import os
import subprocess
import sys
from pathlib import Path


ROOT = Path(__file__).parent.parent


def _read(path: str) -> str:
    return (ROOT / path).read_text()


def test_ci_lint_uses_make_lint() -> None:
    ci = _read(".github/workflows/ci.yml")
    assert "run: make lint" in ci
    assert "flake8 src/ tests/" not in ci


def test_requirements_swap_flake8_for_ruff() -> None:
    requirements = _read("requirements.txt")
    assert "ruff" in requirements
    assert "flake8" not in requirements


def test_make_lint_runs_lint_and_size_checks() -> None:
    makefile = _read("Makefile")
    assert "bash scripts/lint-check.sh && bash scripts/size-check.sh" in makefile


def test_project_init_generates_stable_lint_header() -> None:
    script = _read("scripts/project-init.sh")
    tracked_lint = _read("scripts/lint-check.sh")
    header = "# Generated by project-init.sh. Re-run /project-init to regenerate."
    assert header in script
    assert header in tracked_lint


def test_bootstrap_fails_loudly_when_mcp_entry_missing() -> None:
    bootstrap = _read("scripts/bootstrap.sh")
